/** 入力キーワードに部分一致するサジェスションを返す */
export function getEquipmentSuggestion(input: string): string | null {
  if (!input || input.length < 2) return null;
  const normalized = input.trim();
  // 完全一致
  if (equipmentSuggestions[normalized]) return equipmentSuggestions[normalized];
  // 部分一致
  for (const [key, val] of Object.entries(equipmentSuggestions)) {
    if (key.includes(normalized) || normalized.includes(key)) return val;
  }
  return null;
}
