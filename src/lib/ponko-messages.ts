/**
 * 進捗率に応じたランダムなセリフを返す
 */
export function getPonkoMessage(progressPct: number, seed: number): string {
  const group = MESSAGE_GROUPS.find(
    (g) => progressPct >= g.min && progressPct <= g.max
  ) || MESSAGE_GROUPS[0];

  const idx = seed % group.messages.length;
  return group.messages[idx];
}

/** メッセージの総数を返す */
export function getTotalMessageCount(): number {
  return MESSAGE_GROUPS.reduce((sum, g) => sum + g.messages.length, 0);
}
